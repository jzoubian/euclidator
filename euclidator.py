def read(lines):
    """
    Return acronym dictionary {ACRONYM:definition} from acronym lines.
    """

    d = dict()

    for line in lines:
        line = line.strip()
        if not line or line.startswith('#'):
            continue
        try:
            key, val = line.split(':')       # ACRONYM: definition
        except ValueError:
            print("Error: cannot decipher line", line)
            continue
        val = val.strip()
        if key not in d:
            d[key] = val
        elif val != d[key]:
            print("Merging %s entries: '%s' and '%s'" % (key, val, d[key]))
            d[key] = ' / '.join((d[key], val))

    return d

